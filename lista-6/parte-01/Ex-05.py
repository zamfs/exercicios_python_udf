"""
Fatiamento básico (slicing)
Crie uma lista com os números de 10 a 90 (de 10 em 10). Usando fatiamento, exiba: os três primeiros,
os três últimos e os elementos do índice 2 ao 5 (exclusivo).
"""

numeros = [10,20,30,40,50,60,70,80,90]
print(numeros)

print(numeros[:3])

print(numeros[-3:])

print(numeros[2:5])